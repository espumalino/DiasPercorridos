public class DiasVividos {

    public static void main(String[] args) {

        byte personAge = 18;
        final short DAYS_IN_A_FISCAL_YEAR = 365;
        int daysLived = personAge * DAYS_IN_A_FISCAL_YEAR;

        System.out.println("Você já viveu aproximadamente " + daysLived + " dias!");

    }
}
